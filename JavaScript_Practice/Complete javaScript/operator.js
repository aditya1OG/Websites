// Operator in js 



// arithmatic operator ---->
a = 3
b = 2
console.log("a + b =", a+b)   //5
console.log("a * b =", a-b)     //1
console.log("a * b =", a*b)     //6
console.log("a / b =", a/b)     //1.5
console.log("a % b =", a%b)     //1
console.log("a ** b =", a**b)   //9
console.log("++a =", ++a)      //4          //4 if we console only this value
console.log("a++ =", a++)      //4          //3 if we console only this value
console.log("--a =", --a)      //4          //2 if we console only this value
console.log("a-- =", a--)      //4          //3 if we console only this value
console.log("a =", a)          //3
console.log("a-- =", a--)      //3          //now the value of a is 2


